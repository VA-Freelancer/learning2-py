# type list
nums = [5,10,1,60,25,3]
print("Наибольшое значение:", max(nums))  # максимальное значение
print("Наименьшее значение:", min(nums))  # минимальное значение
print("Сумма значение:", sum(nums))  # сумма значение
print("Список в обратном порядке:", list(reversed(nums)))  # список в обратном значение
print("По возрастанию значений:", sorted(nums))  # Сортировка по возрастанию
print("По убыванию значений:", sorted(nums, reverse=True))  # Сортировка по возрастанию